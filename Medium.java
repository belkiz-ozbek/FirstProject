    /**
     *
     * Determines if the player can move to the specified direction from the
     * current position.
     *
     * @param row the current row position of the player
     * @param col the current column position of the player
     * @param direction the direction to move to, represented as an integer from
     * 37 to 40. 37 corresponds to left, 38 corresponds to up, 39 corresponds to
     * right, and 40 corresponds to down.
     * @return true if the player can move to the specified direction, false
     * otherwise.
     */
    public boolean canMove(int row, int col, int direction) {
        switch (direction) {
            case 38 -> {
                return lab1[row - 1][col] != 1;
            }
            case 40 -> {
                return lab1[row + 1][col] != 1;
            }
            case 37 -> {
                return lab1[row][col - 1] != 1;
            }
            case 39 -> {
                return lab1[row][col + 1] != 1;
            }
            default -> {
            }
        }
        return false;
    }

